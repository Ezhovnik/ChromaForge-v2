#pragma once

#include <typedefs.h>

/**
 * @file voxmaths.h
 * @brief Простые математические утилиты для работы с воксельными данными.
 *
 * Содержит функции целочисленного деления с округлением вниз/вверх,
 * шаблонные минимум/максимум, быстрый LCG-генератор случайных чисел
 * и упаковку цветовых компонентов в 16-битное значение.
 */

/**
 * @brief Целочисленное деление с округлением вниз (к отрицательной бесконечности).
 * @param a Делимое.
 * @param b Делитель (должен быть положительным).
 * @return Наибольшее целое q, такое что q * b <= a.
 *
 * Примеры:
 * floordiv(7, 3) = 2
 * floordiv(-7, 3) = -3
 */
inline int floordiv(int a, int b) {
	if (a < 0 && a % b) return (a / b) - 1;
	return a / b;
}

/**
 * @brief Целочисленное деление с округлением вверх (к положительной бесконечности).
 * @param a Делимое.
 * @param b Делитель (должен быть положительным).
 * @return Наименьшее целое q, такое что q * b >= a.
 *
 * Примеры:
 * ceildiv(7, 3) = 3
 * ceildiv(-7, 3) = -2
 */
inline int ceildiv(int a, int b) {
	if (a > 0 && a % b) return a / b + 1;
	return a / b;
}

/**
 * @brief Возвращает максимум двух значений.
 * @tparam T Тип, поддерживающий operator>.
 * @param a Первое значение.
 * @param b Второе значение.
 * @return a, если a > b, иначе b.
 */
template<typename T>
inline T max(T a, T b) {
	return (a > b) ? a : b;
}

/**
 * @brief Возвращает минимум двух значений.
 * @tparam T Тип, поддерживающий operator<.
 * @param a Первое значение.
 * @param b Второе значение.
 * @return a, если a < b, иначе b.
 */
template<typename T>
inline T min(T a, T b) {
	return (a < b) ? a : b;
}

/// Глобальное состояние для быстрого генератора случайных чисел.
static uint g_seed;

/**
 * @brief Инициализирует генератор fast_rand начальным значением.
 * @param seed Зерно генератора.
 */
inline void fast_srand(int seed) {
	g_seed = seed;
}

/**
 * @brief Быстрый линейный конгруэнтный генератор (LCG).
 * @return Псевдослучайное число в диапазоне [0, 32767].
 *
 * Использует алгоритм: seed = (214013 * seed + 2531011);
 * Возвращает (seed >> 16) & 0x7FFF.
 */
inline int fast_rand(void) {
	g_seed = (214013 * g_seed + 2531011);
	return (g_seed >> 16) & 0x7FFF;
}

/**
 * @brief Упаковывает четыре 4-битных компонента (r, g, b, s) в 16-битное значение.
 * @param r Красный компонент (0–15).
 * @param g Зелёный компонент (0–15).
 * @param b Синий компонент (0–15).
 * @param s Освещение (0-15).
 * @return Упакованное 16-битное значение: биты 0-3 = r, 4-7 = g, 8-11 = b, 12-15 = s.
 *
 * @note Компоненты должны помещаться в 4 бита. Поведение для значений >=16 не определено.
 */
inline light_t light_pack(ubyte r, ubyte g, ubyte b, ubyte s) {
	return r | (g << 4) | (b << 8) | (s << 12);
}
