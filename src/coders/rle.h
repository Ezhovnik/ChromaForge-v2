#pragma once

#include <typedefs.h>

/**
 * @brief Простое RLE-сжатие (длина серии кодируется одним байтом).
 *
 * Алгоритм:
 * - Серия одинаковых байтов записывается как пара (len, byte), где len — количество
 *   повторений минус 1 (от 0 до 255). Таким образом, максимальная длина серии — 256 байт.
 * - Кодировщик автоматически разбивает длинные серии на несколько.
 * - Декодировщик читает пары и восстанавливает исходные данные.
 */
namespace rle {
	/**
     * @brief Кодирует данные с помощью простого RLE.
     * @param src Указатель на исходные данные.
     * @param length Длина исходных данных в байтах.
     * @param dst Указатель на выходной буфер (должен быть достаточного размера).
     * @return Количество байтов, записанных в выходной буфер.
     */
	size_t encode(const ubyte* src, size_t length, ubyte* dst);

	/**
     * @brief Декодирует данные, сжатые простым RLE.
     * @param src Указатель на сжатые данные.
     * @param length Длина сжатых данных в байтах.
     * @param dst Указатель на выходной буфер (достаточного размера).
     * @return Количество байтов, записанных в выходной буфер (размер распакованных данных).
     */
	size_t decode(const ubyte* src, size_t length, ubyte* dst);
}

/**
 * @brief Расширенное RLE-сжатие с поддержкой длинных серий (до 32768 байт).
 *
 * Отличается от простого RLE форматом хранения длины серии:
 * - Если длина серии < 128 (len <= 0x7F), то она хранится в одном байте (старший бит = 0).
 * - Если длина серии >= 128 (len >= 0x80), то она хранится в двух байтах:
 *   первый байт = 0x80 | (len & 0x7F), второй байт = (len >> 7).
 *   Таким образом, максимальная длина серии = 0x7FFF (32767 повторений) + 1 = 32768 байт.
 * Значение len по-прежнему означает (количество повторений - 1).
 */
namespace extrle {
	constexpr uint max_sequence = 0x7FFF; ///< Максимальная длина серии (количество повторений - 1).

	/**
     * @brief Кодирует данные с помощью расширенного RLE.
     * @param src Исходные данные.
     * @param length Длина исходных данных.
     * @param dst Выходной буфер.
     * @return Количество записанных байтов.
     */
	size_t encode(const ubyte* src, size_t length, ubyte* dst);

	/**
     * @brief Декодирует данные, сжатые расширенным RLE.
     * @param src Сжатые данные.
     * @param length Длина сжатых данных.
     * @param dst Выходной буфер.
     * @return Количество распакованных байтов.
     */
	size_t decode(const ubyte* src, size_t length, ubyte* dst);
}
