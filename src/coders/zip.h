#pragma once

#include <vector>

#include <typedefs.h>

/**
 * @brief Пространство имён для работы со сжатием в формате gzip.
 *
 * Использует библиотеку zlib для сжатия и распаковки данных.
 * Формат: gzip (с заголовком и контрольной суммой).
 */
namespace zip {
    /**
     * @brief Магическое число заголовка gzip (0x1F, 0x8B).
     *
     * Может использоваться для проверки, является ли файл gzip-архивом.
     */
    const ubyte MAGIC[] = "\x1F\x8B";

    /**
     * @brief Сжимает данные в формат gzip.
     * @param src Указатель на исходные данные.
     * @param size Размер исходных данных в байтах.
     * @return Вектор байт, содержащий сжатые данные (включая gzip-заголовок).
     *
     * Используется уровень сжатия по умолчанию (Z_DEFAULT_COMPRESSION).
     * В конец сжатых данных дописывается исходный размер (4 байта, little-endian),
     * что позволяет при распаковке заранее выделить буфер.
     */
    std::vector<ubyte> compress(const ubyte* src, size_t size);

    /**
     * @brief Распаковывает данные из формата gzip.
     * @param src Указатель на сжатые данные (gzip-поток).
     * @param size Размер сжатых данных в байтах.
     * @return Вектор байт с распакованными данными.
     *
     * Предполагается, что в конце сжатых данных записан исходный размер
     * (те же 4 байта, что добавляет compress). Если это не так, поведение не определено.
     */
    std::vector<ubyte> decompress(const ubyte* src, size_t size);
}
