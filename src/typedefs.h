#pragma once

#include <stdint.h>
#include <memory>

/**
 * @file typedefs.h
 * @brief Определения сокращённых имён для базовых типов и специализированных типов проекта.
 *
 * Все используемые в проекте типы объявлены здесь для обеспечения переносимости
 * и единообразия. Для целочисленных типов фиксированного размера используются
 * стандартные определения из <stdint.h>.
 */

using scriptenv = std::shared_ptr<int>;
using observer_handler = std::shared_ptr<int>;

// ========== Базовые типы (сокращения) ==========

/** Беззнаковый целый тип (обычно 4 байта). */
using uint = unsigned int;
/** Беззнаковый короткий целый тип (обычно 2 байта). */
using ushort = unsigned short;
/** Беззнаковый длинный целый тип. */
using ulong = unsigned long;
/** Однобайтовый беззнаковый тип (байт). */
using ubyte = uint8_t;

// ========== Типы для игровых сущностей ==========

/** Тип идентификатора предмета. */
using itemid_t = uint32_t;
/** Тип идентификатора блока. */
using blockid_t = uint16_t;
/** Тип идентификатора сущности. */
using entityid_t = uint64_t;
/** Тип для хранения уровня освещения (16 бит). */
using light_t = uint16_t;
/** Тип состояния блока (16 бит) */
using blockstate_t = uint16_t;
/** Тип количества предметов в стаке (32 бита). */
using itemcount_t = uint32_t;

// ========== Типы для динамических контейнеров ==========
/** Целочисленный тип (64 битное знаковое целое число) */
using integer_t = int64_t;
/** Число с плавающей точкой (64 битное знаковое вещественное число) */
using number_t = double;
